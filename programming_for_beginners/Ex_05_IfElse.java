/*
 * #5 - Conditional if else statement in Java
Programming
if() {
execute this
}
else {
execute this
}
 */

public class Ex_05_IfElse {
    public static void main(String[] args) {
        int number = 10;
        if (number < 10) {
            System.out.println("number is less than 10");
        } else {
            System.out.println("number is not less than 10");
        }

    }
}
